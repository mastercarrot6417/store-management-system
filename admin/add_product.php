    $category     = trim($_POST['category']     ?? '');
    $sub_category = trim($_POST['sub_category'] ?? '');
    $brand        = trim($_POST['brand']        ?? '');
    $status       = trim($_POST['status']       ?? 'ACTIVE');
    $price        = floatval($_POST['price']    ?? 0);
    $cost_price   = $isBoss ? floatval($_POST['cost_price'] ?? 0) : 0;
    $online_sell_price = $isBoss ? floatval($_POST['online_sell_price'] ?? 0) : 0;
    $description  = trim($_POST['description']  ?? '');
    $arrival_date = $_POST['arrival_date']      ?? date('Y-m-d');

    // Validation
    if (empty($item_code) || empty($name) || empty($category) || empty($sub_category) || empty($brand)) {
        $error = 'Please fill in all required fields.';
    } else {
        $check = $conn->prepare("SELECT id FROM products WHERE item_code = ?");
        $check->execute([$item_code]);
        if ($check->fetch()) {
            $error = 'Item code "' . htmlspecialchars($item_code) . '" already exists.';
        }
    }

    if (!$error) {
        $stmt = $conn->prepare("INSERT INTO products (item_code, name, brand, category, sub_category, status, price, cost_price, online_sell_price, description, arrival_date) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?) RETURNING id");
        if ($stmt->execute([$item_code, $name, $brand, $category, $sub_category, $status, $price, $cost_price, $online_sell_price, $description, $arrival_date])) {
            $product_id = (int) $stmt->fetchColumn();

            $_SESSION['success'] = 'Product "' . htmlspecialchars($name) . '" added! Now set up its colors and images.';
            header("Location: manage_product_colors.php?product_id=$product_id&new=1");
            exit();
        } else {
            $error = 'Failed to save product. Please try again.';
        }
    }
    }
}

