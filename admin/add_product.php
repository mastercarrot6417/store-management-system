<script>
const subCategories = {
    Helmet:      ['Full Face Helmet','Open Face Helmet','Flip Up Helmet','Off Road Helmet','Kid Helmet'],
    Apparel:     ['Jackets','Pants','Gloves','Rain Gear'],
    Accessories: ['Bags','Disc Lock','Helmet Accessories','Other Accessories'],
};

function updateSubCategories(cat, selected) {
    const sel = document.getElementById('sub_category');
    sel.innerHTML = '<option value="">-- Select Sub Category --</option>';
    (subCategories[cat] || []).forEach(s => {
        const o = document.createElement('option');
        o.value = o.textContent = s;
        if (s === selected) o.selected = true;
        sel.appendChild(o);
    });
}

// Restore sub-category on page reload (validation error)
(function() {
    const cat = document.getElementById('category').value;
    const sub = '<?php echo addslashes($_POST['sub_category'] ?? ''); ?>';
    if (cat) updateSubCategories(cat, sub);
})();

</script>

