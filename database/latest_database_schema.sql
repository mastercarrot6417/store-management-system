-- ==========================================================
-- Product colour + size stock table
-- ==========================================================
CREATE TABLE IF NOT EXISTS product_sizes (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id BIGINT NOT NULL REFERENCES products(id) ON DELETE CASCADE,
  color_id BIGINT REFERENCES product_colors(id) ON DELETE CASCADE,
  size TEXT NOT NULL,
  quantity INTEGER NOT NULL DEFAULT 0,
  created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW()
);

ALTER TABLE product_sizes
ADD COLUMN IF NOT EXISTS color_id BIGINT,
ADD COLUMN IF NOT EXISTS created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW(),
ADD COLUMN IF NOT EXISTS updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW();

-- Assign old NULL colour stock rows to a default colour before the new unique rule.
INSERT INTO product_colors (product_id, color_name, color_code, is_default)
SELECT p.id, 'Default', '#000000', TRUE
FROM products p
WHERE EXISTS (
    SELECT 1 FROM product_sizes ps
    WHERE ps.product_id = p.id AND ps.color_id IS NULL
)
AND NOT EXISTS (
    SELECT 1 FROM product_colors pc
    WHERE pc.product_id = p.id AND LOWER(pc.color_name) = 'default'
);

UPDATE product_sizes ps
SET color_id = pc.id
FROM product_colors pc
WHERE ps.product_id = pc.product_id
  AND ps.color_id IS NULL
  AND LOWER(pc.color_name) = 'default';

-- Merge duplicate stock rows for same product + colour + size before adding unique rule.
WITH duplicate_groups AS (
    SELECT
        product_id,
        color_id,
        size,
        MIN(id) AS keep_id,
        SUM(COALESCE(quantity, 0)) AS total_quantity
    FROM product_sizes
    GROUP BY product_id, color_id, size
    HAVING COUNT(*) > 1
), update_kept AS (
    UPDATE product_sizes ps
    SET quantity = dg.total_quantity
    FROM duplicate_groups dg
    WHERE ps.id = dg.keep_id
    RETURNING ps.id
)
DELETE FROM product_sizes ps
USING duplicate_groups dg
WHERE ps.product_id = dg.product_id
  AND ps.color_id IS NOT DISTINCT FROM dg.color_id
  AND ps.size = dg.size
  AND ps.id <> dg.keep_id;

-- Remove old unique rule and apply latest colour + size unique rule.
ALTER TABLE product_sizes
DROP CONSTRAINT IF EXISTS product_sizes_product_id_size_key;

ALTER TABLE product_sizes
DROP CONSTRAINT IF EXISTS product_sizes_product_color_size_key;

ALTER TABLE product_sizes
ADD CONSTRAINT product_sizes_product_color_size_key
UNIQUE (product_id, color_id, size);

-- ==========================================================
-- Stock movement history / audit table
-- ==========================================================
CREATE TABLE IF NOT EXISTS stock_history (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  product_id BIGINT NOT NULL REFERENCES products(id) ON DELETE CASCADE,
  color_id BIGINT REFERENCES product_colors(id) ON DELETE SET NULL,
  size TEXT NOT NULL,
  type TEXT NOT NULL CHECK (type IN ('IN', 'OUT')),
  quantity INTEGER NOT NULL,
  reason TEXT DEFAULT '',
  quantity_before INTEGER DEFAULT 0,
  quantity_after INTEGER DEFAULT 0,
  note TEXT,
  edited_by_admin_id BIGINT,
  edited_by_admin_name TEXT,
  created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW(),
  updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW()
);

ALTER TABLE stock_history
ADD COLUMN IF NOT EXISTS color_id BIGINT,
ADD COLUMN IF NOT EXISTS quantity_before INTEGER DEFAULT 0,
ADD COLUMN IF NOT EXISTS quantity_after INTEGER DEFAULT 0,
ADD COLUMN IF NOT EXISTS note TEXT,
ADD COLUMN IF NOT EXISTS edited_by_admin_id BIGINT,
ADD COLUMN IF NOT EXISTS edited_by_admin_name TEXT,
ADD COLUMN IF NOT EXISTS created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW(),
ADD COLUMN IF NOT EXISTS updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT NOW();

UPDATE stock_history
SET quantity_before = 0
WHERE quantity_before IS NULL;

UPDATE stock_history
SET quantity_after = quantity
WHERE quantity_after IS NULL;

UPDATE stock_history
SET edited_by_admin_name = 'Not recorded'
WHERE edited_by_admin_name IS NULL OR edited_by_admin_name = '';

